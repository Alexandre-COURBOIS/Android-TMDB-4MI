package mi.videoprime.model;

import java.util.List;

public class MovieCredits {

    private int id;
    private List<Actor> cast;

    // Getters and setters
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public List<Actor> getCast() {
        return cast;
    }

    public void setCast(List<Actor> cast) {
        this.cast = cast;
    }
}
